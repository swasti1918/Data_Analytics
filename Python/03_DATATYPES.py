# ==============================
# DATA TYPES IN PYTHON
# ==============================

# 1. INTEGER (int)
a = 10
print("Integer:", a, type(a))

# 2. FLOAT (float)
b = 5.5
print("Float:", b, type(b))

# 3. STRING (str)
name = "Sanyam"
print("String:", name, type(name))

# 4. BOOLEAN (bool)
is_student = True
print("Boolean:", is_student, type(is_student))

# ==============================
# COLLECTION DATA TYPES
# ==============================

# 5. LIST (mutable)
my_list = [1, 2, 3, "hello"]
print("List:", my_list, type(my_list))

# 6. TUPLE (immutable)
my_tuple = (10, 20, 30)
print("Tuple:", my_tuple, type(my_tuple))

# 7. SET (unique values)
my_set = {1, 2, 3, 3}
print("Set:", my_set, type(my_set))

# 8. DICTIONARY (key-value pair)
my_dict = {"name": "Sanyam", "age": 20}
print("Dictionary:", my_dict, type(my_dict))

# ==============================
# SPECIAL DATA TYPE
# ==============================

# 9. NONE TYPE
x = None
print("None:", x, type(x))